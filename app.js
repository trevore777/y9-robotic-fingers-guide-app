const stateKey = "y9RoboticFingersProgress";
let saved = JSON.parse(localStorage.getItem(stateKey) || "{}");

function save() {
  localStorage.setItem(stateKey, JSON.stringify(saved));
  updateProgress();
}

function updateProgress() {
  const boxes = [...document.querySelectorAll("input[type='checkbox']")];
  const done = boxes.filter(b => b.checked).length;
  const pct = boxes.length ? Math.round((done / boxes.length) * 100) : 0;
  document.getElementById("progressLabel").textContent = `${pct}% complete`;
  document.getElementById("progressBar").style.width = `${pct}%`;
}

function addCheckbox(id, label, subtext = "") {
  const wrap = document.createElement("label");
  wrap.className = "lesson";
  const box = document.createElement("input");
  box.type = "checkbox";
  box.checked = !!saved[id];
  box.addEventListener("change", () => {
    saved[id] = box.checked;
    save();
  });
  const text = document.createElement("span");
  text.innerHTML = `<strong>${label}</strong>${subtext ? `<small>${subtext}</small>` : ""}`;
  wrap.append(box, text);
  return wrap;
}

function renderEvidence() {
  const list = document.getElementById("evidenceList");
  evidenceItems.forEach((item, index) => {
    const li = document.createElement("li");
    li.appendChild(addCheckbox(`evidence-${index}`, item));
    list.appendChild(li.firstChild);
  });
}

function renderWeeks() {
  const root = document.getElementById("weeks");
  const template = document.getElementById("weekTemplate");
  weeks.forEach(week => {
    const node = template.content.cloneNode(true);
    const card = node.querySelector(".week-card");
    if (week.week === 1) card.classList.add("open");
    node.querySelector(".week-pill").textContent = `Week ${week.week}`;
    node.querySelector(".week-title").textContent = week.title;
    node.querySelector(".focus").textContent = week.focus;
    node.querySelector(".benchmark").textContent = week.benchmark;
    node.querySelector(".tasks").textContent = `Folio focus: ${week.tasks}`;

    node.querySelector(".week-head").addEventListener("click", () => {
      card.classList.toggle("open");
    });

    const lessons = node.querySelector(".lesson-list");
    week.lessons.forEach((lesson, i) => {
      const [code, topic, objective, evidence] = lesson;
      lessons.appendChild(addCheckbox(
        `week-${week.week}-lesson-${i}`,
        `${code} · ${topic}`,
        `${objective} · Evidence: ${evidence}`
      ));
    });
    root.appendChild(node);
  });
}

